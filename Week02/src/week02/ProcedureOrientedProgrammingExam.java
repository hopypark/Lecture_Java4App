package week02;

//일어나서 학교가기 프로그램
public class ProcedureOrientedProgrammingExam {
	
	public static void main(String[] args) {	// main() 함수는 프로그램 시작
		getUp();		// 일어난다.
		wash();			// 씻는다.
		eatBreakfast();	// 밥을 먹는다.
		takeBus();		// 버스를 탄다.
		chargeFee();	// 요금을 지불한다.
		arriveSchool();	// 학교에 도착한다.
	}											// 프로그램 종료

	public static void getUp() {}			// 일어난다.
	public static void wash() {}			// 씻는다.
	public static void eatBreakfast() {}	// 밥을 먹는다.
	public static void takeBus() {}		// 버스를 탄다.
	public static void chargeFee() {}		// 요금을 지불한다.
	public static void arriveSchool() {}	// 학교에 도착한다.
}


